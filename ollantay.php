            El templo de Ollantaytambo es un conjunto arquitectónico impresionante.
            Se encuentra situado a 2792 metros de altitud y tiene una antigüedad de 3500 años. Se cree que su construcción fue orientada en dirección hacia la ciudad del Cusco, pues sus arquitectos buscaban defenderse de los Incas. Asimismo, eligieron esta zona por los canales de aguas subterráneas que descubrieron a gran profundidad. Los constructores de Ollantaytambo diseñaron un sistema de acueductos que actualmente sigue en pleno funcionamiento. Cada fuente fue labrada en roca con sus propias características y diseños.
