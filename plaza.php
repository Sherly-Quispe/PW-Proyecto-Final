            La vara de oro se hundió aquí y los fundadores del Imperio Inca comenzaron a preparar el terreno para construir lo que sería su capital.
            El inca Sinchi Roca, sucesor de Manco Cápac, ordenó el secado de este pantano para transformar el lugar en el centro administrativo, cultural y religioso de lo que sería el gran Imperio Inca.
            Durante la época Inca, la Plaza de Armas ocupaba un espacio mucho más extenso del que conocemos ahora, 
            incluyendo diversos lugares de los alrededores. Así, durante el incanato la plaza se dividía en dos partes: Aucaypata, Huacaypata o Huacapata (los estudiosos no se ponen de acuerdo en el nombre original) y Cusipata,
            las cuales estaban separadas por el riachuelo Saphi.
            <p>En la Plaza de Armas fue, además, se encontraban los palacios de incas de gran importancia como Pachacútec, 
            Huayna Cápac o Viracocha y aquí se celebraban grandes eventos como el famoso Inti Raymi. Por otro lado, 
            este era el punto de partida de la gran red de caminos incas, el Qhapaq Ñan, que conectaban a todo el imperio.
            Como verás, la Plaza de Armas de Cusco era la representación de la fuerza
            y la hegemonía de los incas en la región Sudamericana. Pero con la llegada de los españoles toda su suerte cambió.
