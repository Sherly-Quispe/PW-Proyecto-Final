            <p>
            La construcción de Pisac se realizó durante el gobierno del emperador Pachacutec en el siglo XV, durante una época de expansión territorial vertiginosa.
            En el siglo XVI, tras la llegada de los españoles a Cusco, Pisac fue invadido y sus pobladores huyeron. Sus principales monumentos fueron dañados. El cementerio inca fue destruido y saqueado.
            Con las reformas toledanas (entre 1532 y 1600), Pisac se convirtió en una reducción de indios. Con el transcurrir del tiempo, el pueblo se estableció en las faldas de la montaña donde se asienta el sitio arqueológico inca.
            Hoy, Pisac es uno de los atractivos turísticos más famosos del Valle Sagrado de los Incas. El sitio arqueológico inca conserva muchas de sus estructuras originales en buen estado de conservación.
